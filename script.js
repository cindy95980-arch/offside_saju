function calculateSaju(dateString) {
    const target = new Date(dateString);
    const base = new Date(2024, 0, 1);
    const diffTime = Date.UTC(target.getFullYear(), target.getMonth(), target.getDate()) - Date.UTC(base.getFullYear(), base.getMonth(), base.getDate());
    const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));
    let playerIdx = diffDays % 60;
    if (playerIdx < 0) playerIdx += 60;
    return playerIdx;
}

